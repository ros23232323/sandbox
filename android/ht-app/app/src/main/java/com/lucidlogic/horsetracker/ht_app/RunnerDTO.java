public class RunnerDTO {
  public String getAge() {
    return this.age;
  }

  public void setAge(final String age) {
    this.age = age;
  }

  public String getBreeding() {
    return this.breeding;
  }

  public void setBreeding(final String breeding) {
    this.breeding = breeding;
  }

  public String getForm() {
    return this.form;
  }

  public void setForm(final String form) {
    this.form = form;
  }

  public String getHorse() {
    return this.horse;
  }

  public void setHorse(final String horse) {
    this.horse = horse;
  }

  public String getOr() {
    return this.or;
  }

  public void setOr(final String or) {
    this.or = or;
  }

  public String getProfileUrl() {
    return this.profileUrl;
  }

  public void setProfileUrl(final String profileUrl) {
    this.profileUrl = profileUrl;
  }

  public boolean isRunning() {
    return this.running;
  }

  public void setRunning(final boolean running) {
    this.running = running;
  }

  public String getSlikImgUrl() {
    return this.slikImgUrl;
  }

  public void setSlikImgUrl(final String slikImgUrl) {
    this.slikImgUrl = slikImgUrl;
  }

  public String getSp() {
    return this.sp;
  }

  public void setSp(final String sp) {
    this.sp = sp;
  }

  public String getStall() {
    return this.stall;
  }

  public void setStall(final String stall) {
    this.stall = stall;
  }

  public String getWeight() {
    return this.weight;
  }

  public void setWeight(final String weight) {
    this.weight = weight;
  }

  private String age;

  private String breeding;

  private String form;

  private String horse;

  private String or;

  private String profileUrl;

  private boolean running;

  private String slikImgUrl;

  private String sp;

  private String stall;

  private String weight;

}
